// Example downlines data
const downlines = [
  { username: "maryKenya", phone: "0712345678", date: "2024-06-10" },
  { username: "samUganda", phone: "0756789123", date: "2024-06-11" },
  { username: "janeTz", phone: "0787654321", date: "2024-06-12" }
  // Add more as needed
];

function renderDownlines() {
  const tbody = document.getElementById('downlinesBody');
  tbody.innerHTML = "";
  downlines.forEach((d, idx) => {
    const tr = document.createElement('tr');
    tr.innerHTML = `
      <td>${idx + 1}</td>
      <td>${d.username}</td>
      <td>${d.phone}</td>
      <td>${d.date}</td>
    `;
    tbody.appendChild(tr);
  });
}

window.onload = renderDownlines;